                    <p>ก่อตั้งขึ้นเมื่อวันที่ 10 พฤศจิกายน 2540  ดำเนินการโดย คุณ บุญฑริกา ศุลกะนุเคราะห์ และ คุณ สุพิญญา ดาวกาษ  <br>เดิมที คุณ บุญฑริกา เป็นคนจังหวัดเชียงรายและได้ย้ายเข้ามาเรียนต่อในกรุงเทพได้พบกับคุณ สุพิญญา ซึ่งทั้ง 2 คน <br>ต่างมีจิตใจที่เมตตาเหมือนกันจึงทำให้หลังจากเรียนจบ ทั้ง 2 คน ได้ไปบริจาคสิ่งของและทำบุญบริจาคช่วยเหลือเด็กกำพร้า<br>อยู่ตลอดจนวันที่ 10 พฤศจิกายน 2540 คุณ บุญฑริกา ได้คุยกับ สุพิญญา ว่า เราเป็นคนจังหวัดเชียงรายที่หมู่บ้านของเรา<br>มีเด็กกำพร้าและยากไร้เป็นจำนวนมาก เราควรที่จะเปิดรับบริจาคสิ่งของและเงินบริจาคเพื่อที่จะไปช่วยเหลือเด็กเหล่านี้กัน<br>จึงทำให้วันที่ 10 พฤศจิกายน 2540 เป็นวันก่อตั้งองค์กร</p>
					
